package com.ninhongo.entities.jmdicmodel;

public class SenseLanguageSourcedicc {
	
	private boolean full;
	private String lang;
	private String text; 
	private boolean wasei;
	
	public boolean isFull() {
		return full;
	}
	public void setFull(boolean full) {
		this.full = full;
	}
	public String getLang() {
		return lang;
	}
	public void setLang(String lang) {
		this.lang = lang;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public boolean isWasei() {
		return wasei;
	}
	public void setWasei(boolean wasei) {
		this.wasei = wasei;
	}
	
}
